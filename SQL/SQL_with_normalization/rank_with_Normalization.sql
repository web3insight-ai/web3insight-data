                    GROUP BY t1.repo_id),
    -- 这里进行归一化，将每个指标进行 Min-Max 归一化
    repo_metric_norm AS (SELECT repo_id,
                                -- 日志压缩
                                LOG(active_actor_count + 1)         AS raw_active_actor,
                                LOG(none_active_actor_count + 1)    AS raw_none_active_actor,
                                LOG(active_actor_pr_count + 1)      AS raw_active_pr,
                                LOG(none_active_actor_pr_count + 1) AS raw_none_active_pr,
                                LOG(star_count + 1)                 AS raw_star,
                                LOG(fork_count + 1)                 AS raw_fork,

                                -- Min-Max 归一化
                                (LOG(active_actor_count + 1) - MIN(LOG(active_actor_count + 1)) OVER ()) /
                                NULLIF(MAX(LOG(active_actor_count + 1)) OVER () -
                                       MIN(LOG(active_actor_count + 1)) OVER (), 0)
                                                                    AS norm_active_actor,
                                (LOG(none_active_actor_count + 1) - MIN(LOG(none_active_actor_count + 1)) OVER ()) /
                                NULLIF(MAX(LOG(none_active_actor_count + 1)) OVER () -
                                       MIN(LOG(none_active_actor_count + 1)) OVER (), 0)
                                                                    AS norm_none_active_actor,
                                (LOG(active_actor_pr_count + 1) - MIN(LOG(active_actor_pr_count + 1)) OVER ()) /
                                NULLIF(MAX(LOG(active_actor_pr_count + 1)) OVER () -
                                       MIN(LOG(active_actor_pr_count + 1)) OVER (), 0)
                                                                    AS norm_active_pr,
                                (LOG(none_active_actor_pr_count + 1) -
                                 MIN(LOG(none_active_actor_pr_count + 1)) OVER ()) /
                                NULLIF(MAX(LOG(none_active_actor_pr_count + 1)) OVER () -
                                       MIN(LOG(none_active_actor_pr_count + 1)) OVER (), 0)
                                                                    AS norm_none_active_pr,
                                (LOG(star_count + 1) - MIN(LOG(star_count + 1)) OVER ()) /
                                NULLIF(MAX(LOG(star_count + 1)) OVER () - MIN(LOG(star_count + 1)) OVER (), 0)
                                                                    AS norm_star,
                                (LOG(fork_count + 1) - MIN(LOG(fork_count + 1)) OVER ()) /
                                NULLIF(MAX(LOG(fork_count + 1)) OVER () - MIN(LOG(fork_count + 1)) OVER (), 0)
                                                                    AS norm_fork
                         FROM repo_metric),
    repo_eco AS (SELECT t1.repo_id, t1.repo_name, t1.eco_names, t1.eco_count
                 FROM (SELECT repo_id,
                              MAX(repo_name)                  AS repo_name,
                              ARRAY_AGG(key)                  AS eco_names,
                              ARRAY_LENGTH(ARRAY_AGG(key), 1) AS eco_count
                       FROM web3.repos, JSONB_EACH(upstream_marks)
                       WHERE key <> 'ALL'
                         AND (value -> 'branch') = '[]'::jsonb
                       GROUP BY repo_id) t1
                          JOIN eco_list el ON t1.eco_names && el.eco_names),
    eco_metric AS (SELECT UNNEST(repo_eco.eco_names)                   AS eco_name,
                          repo_eco.repo_name,
                          (
                              norm_active_actor * 0.4 +
                              norm_none_active_actor * 0.3 +
                              norm_active_pr * 0.3 +
                              norm_none_active_pr * 0.2 +
                              norm_star * 0.1 +
                              norm_fork * 0.1
                              ) * EXP(-0.1 * (repo_eco.eco_count - 1)) AS score
                   FROM repo_metric_norm
                            JOIN repo_eco ON repo_metric_norm.repo_id = repo_eco.repo_id),
    final_table AS (SELECT eco_name,
                           repo_name,
                           RANK() OVER (PARTITION BY eco_name ORDER BY score DESC) AS rn,
                           score
                    FROM eco_metric
                             JOIN eco_list ON eco_metric.eco_name = ANY (eco_list.eco_names))
SELECT eco_name, repo_name, rn, score
FROM final_table
WHERE rn <= 10
ORDER BY eco_name, rn;